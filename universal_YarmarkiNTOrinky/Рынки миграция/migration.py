def normStr(s):
    if s is None:
        return None
    s = str(s).strip()
    return s if s else None

def split_postal_address(s):
    raw = str(s).strip()
    if not raw:
        return {"postalCode": None, "fullAddress": None}

    # "236006, РљР°Р»РёРЅРёРЅРіСЂР°Рґ ..." -> postalCode=236006, fullAddress="РљР°Р»РёРЅРёРЅРіСЂР°Рґ ..."
    m = re.match(r"^(\d{6})(?:,\s*)?(.*)$", raw)
    if not m:
        return {"postalCode": None, "fullAddress": raw}

    postal_code = m.group(1)
    rest = (m.group(2) or "").strip()

    if not rest:
        rest = None

    return {
        "postalCode": postal_code,
        "fullAddress": rest
    }

def norm_key(value):
    if value is None:
        return ""
    return re.sub(r"\s+", " ", str(value).strip().lower())

def dash_str(value):
    value = normStr(value)
    return value if value is not None else "-"

def format_date_to_dmy(value):
    text = normStr(value)
    if not text:
        return None
    try:
        dt = pd.to_datetime(text, errors="coerce", dayfirst=True)
        if pd.isna(dt):
            return None
        return dt.strftime("%d.%m.%Y")
    except Exception:
        return None

def collect_non_empty_values(row, column_names):
    values = []
    for column_name in column_names:
        value = normStr(row.get(column_name))
        if value is not None:
            values.append(value)
    return values

